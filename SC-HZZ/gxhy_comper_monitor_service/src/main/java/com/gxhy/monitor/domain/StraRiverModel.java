package com.gxhy.monitor.domain;

import java.util.Date;

public class StraRiverModel {
  
	private String uuid;
	
	private Date creatTm; 
	
	private String rvCd;
	
	private String rvNm;
	
	private String straFileTitle;
	
	private String straFileContent;
	
	private int type;
    
	private int downloadNum;//下载次数
	
	private int checkNum;//查看次数
	
	private String typeNm;//类型名称
	
	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

	public Date getCreatTm() {
		return creatTm;
	}

	public void setCreatTm(Date creatTm) {
		this.creatTm = creatTm;
	}

	public String getRvCd() {
		return rvCd;
	}

	public void setRvCd(String rvCd) {
		this.rvCd = rvCd;
	}

	public String getStraFileTitle() {
		return straFileTitle;
	}

	public void setStraFileTitle(String straFileTitle) {
		this.straFileTitle = straFileTitle;
	}

	public String getStraFileContent() {
		return straFileContent;
	}

	public void setStraFileContent(String straFileContent) {
		this.straFileContent = straFileContent;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getRvNm() {
		return rvNm;
	}

	public void setRvNm(String rvNm) {
		this.rvNm = rvNm;
	}
	public int getCheckNum() {
		return checkNum;
	}

	public void setCheckNum(int checkNum) {
		this.checkNum = checkNum;
	}

	public int getDownloadNum() {
		return downloadNum;
	}

	public void setDownloadNum(int downloadNum) {
		this.downloadNum = downloadNum;
	}

	public String getTypeNm() {
		return typeNm;
	}

	public void setTypeNm(String typeNm) {
		this.typeNm = typeNm;
	}

}
